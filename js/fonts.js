export const FONTS = {
  '5x7': { glyphs: F57, height: 7, label: '5×7' },
  '3x5': { glyphs: F35, height: 5, label: '3×5' },
};

// Stamp text into a cells object. Returns { cells, width } where each cell has
// a `ci` char-index used by typewriter/per-char animations.
export function textToCells(text, { font = '5x7', tracking = 1, row = 0, col = 0, cellFactory }) {
  const f = FONTS[font] || FONTS['5x7'];
  const cells = {};
  let cursor = col;
  const chars = [...String(text).toUpperCase()];
  chars.forEach((ch, ci) => {
    const glyph = f.glyphs[ch] || f.glyphs['?'] || f.glyphs[' '];
    const w = glyph[0].length;
    for (let gr = 0; gr < glyph.length; gr++) {
      for (let gc = 0; gc < w; gc++) {
        if (glyph[gr][gc] === '#') {
          const cell = cellFactory();
          cell.ci = ci;
          cells[(row + gr) + ',' + (cursor + gc)] = cell;
        }
      }
    }
    cursor += w + tracking;
  });
  return { cells, width: cursor - col - tracking, height: f.height };
}
